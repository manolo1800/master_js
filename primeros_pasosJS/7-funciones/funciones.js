'use strict'

function sumar()
{
    var numero1=parseInt(document.getElementById('numero1').value);
    var numero2=parseInt(document.getElementById('numero2').value);
    alert(numero1+numero2);
}

function restar()
{
    var numero1=parseInt(document.getElementById('numero1').value);
    var numero2=parseInt(document.getElementById('numero2').value);
    alert(numero1-numero2);
}

function multiplicar()
{
    var numero1=parseInt(document.getElementById('numero1').value);
    var numero2=parseInt(document.getElementById('numero2').value);
    alert(numero1*numero2);
}

function dividir()
{
    var numero1=parseInt(document.getElementById('numero1').value);
    var numero2=parseInt(document.getElementById('numero2').value);
    alert(numero1/numero2);
}
